package javabasico;
/*
 * Atividade 13. Declara duas variáveis numéricas (com o valor que quiseres), mostra por consola a soma, subtração, multiplicação, divisão e módulo (resto da divisão).
 */

public class Atividade13 {

	public static void main(String[] args) {
		
		int var1 = 16;
		int var2 = 64;
		
		System.out.println("O resultado da soma é "+(var1+var2));
		System.out.println("O resultado da subtração é "+(var1-var2)); 
		System.out.println("O resultado da multiplicação é "+(var1*var2)); 
		System.out.println("O resultado da divisão é "+(var1/var2));
	}

}
